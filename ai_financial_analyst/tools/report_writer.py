"""Report writer — structured Markdown report assembly from Quant output.

Two-call strategy:
  Call 1 (subllm / Flash-Lite): Executive Summary, Data Coverage, Financial Overview
  Call 2 (primary_llm / Flash):  Quantitative Analysis, Bull Case, Bear Case, Conclusion

LLMs are passed explicitly as keyword arguments — no module-level globals, no
configure() call. The orchestrator adds `subllm` to node_config and the editor
passes it through directly.
"""

from __future__ import annotations

import logging
import re
from typing import Any

from langchain_core.messages import HumanMessage, SystemMessage

from ..core.llm import content_to_str

logger = logging.getLogger(__name__)

# Sections 1–3: structural / coverage — generated by subllm (Flash-Lite)
_STRUCTURAL_SYSTEM = """You are a senior financial analyst editor.
Generate ONLY these three Markdown sections from the analysis JSON provided:

1. # Executive Summary  (2-3 sentences: key finding, overall recommendation)
2. ## Data Coverage Summary  (table: ticker ✓/✗ for each data type; list any data gaps)
3. ## Financial Overview  (current price, market cap, PE ratio, sector — one paragraph)

Rules:
- Use ONLY numbers that appear in the analysis JSON. Do NOT invent figures.
- Format numbers professionally: $4.17T, 27.2%, 34.4x P/E. Round to 3 significant figures.
- Every quantitative claim MUST cite source: (Source: fundamentals), (Source: price_history).
- Output ONLY the three sections above — no other text."""

# Sections 4–7: analytical — generated by primary_llm (Flash)
_ANALYTICAL_SYSTEM = """You are a senior financial analyst editor.
Generate ONLY these four Markdown sections from the analysis JSON provided:

4. ## Quantitative Analysis  (CAGR, Sharpe ratio, P/E vs sector, FCF yield, peer comparison)
5. ## Bull Case  (bullet list of 2-3 specific data-driven reasons to be optimistic)
6. ## Bear Case  (bullet list of 2-3 concrete risks with cited figures)
7. ## Conclusion  (final investment perspective, 2-3 sentences)

Rules:
- Use ONLY numbers that appear in the analysis JSON. Do NOT invent figures.
- Format numbers professionally: $4.17T, 27.2%, 34.4x P/E. Round to 3 significant figures.
- Every quantitative claim MUST cite source: (Source: price_metrics), (Source: benchmark_lookup).
- Do NOT add [UNVERIFIED] tags — grounding is handled in post-processing.
- Output ONLY the four sections above — no other text."""

# Required section headings for post-generation validation
_REQUIRED_SECTIONS = [
    "Executive Summary",
    "Data Coverage",
    "Financial Overview",
    "Quantitative Analysis",
    "Bull Case",
    "Bear Case",
    "Conclusion",
]

_DISCLAIMER = (
    "\n\n---\n"
    "*DISCLAIMER: This report was generated by an AI system. "
    "All figures should be independently verified before making any investment "
    "decisions. This is not financial advice.*"
)

# Minimum word counts per section — used by _check_quality() to log warnings.
# These thresholds flag stub/empty sections; they do not cause report failure.
_MIN_SECTION_WORDS: dict[str, int] = {
    "Executive Summary":    50,
    "Data Coverage":        10,
    "Financial Overview":   30,
    "Quantitative Analysis": 80,
    "Bull Case":            40,
    "Bear Case":            40,
    "Conclusion":           30,
}

# Matches section headings: # Heading or ## Heading
_SECTION_HEADING_RE = re.compile(r"^#{1,3}\s+(.+)$", re.MULTILINE)


async def write_report(
    analysis_json: str,
    tickers: list[str],
    data_gaps: list[str],
    *,
    primary_llm: Any,
    subllm: Any = None,
) -> str:
    """Assemble the final Markdown report from quantitative analysis JSON.

    Uses two LLM calls: subllm (Flash-Lite) for structural sections,
    primary_llm (Flash) for analytical sections.
    Validates mandatory sections and appends the disclaimer footer.
    """
    if primary_llm is None:
        raise RuntimeError("write_report requires primary_llm")

    structural_llm = subllm if subllm is not None else primary_llm

    # Call 1: structural sections (cheap)
    structural_response = await structural_llm.ainvoke([
        SystemMessage(content=_STRUCTURAL_SYSTEM),
        HumanMessage(content=f"Analysis JSON:\n\n{analysis_json}"),
    ])
    structural_raw = structural_response.content if hasattr(structural_response, "content") else structural_response
    structural_part = content_to_str(structural_raw).strip()

    # Call 2: analytical sections (full reasoning)
    analytical_response = await primary_llm.ainvoke([
        SystemMessage(content=_ANALYTICAL_SYSTEM),
        HumanMessage(content=f"Analysis JSON:\n\n{analysis_json}"),
    ])
    analytical_raw = analytical_response.content if hasattr(analytical_response, "content") else analytical_response
    analytical_part = content_to_str(analytical_raw).strip()

    report = structural_part + "\n\n" + analytical_part
    report = _validate_sections(report)
    report = _enforce_disclaimer(report)
    _check_quality(report)  # logs warnings only; does not alter the report
    return report


def _validate_sections(report: str) -> str:
    """Ensure all mandatory section headings are present; add stubs for missing ones."""
    for section in _REQUIRED_SECTIONS:
        if f"## {section}" not in report and f"# {section}" not in report:
            logger.warning("Report missing mandatory section: %s — appending stub", section)
            report += f"\n\n## {section}\n*(Section could not be generated)*\n"
    return report


def _enforce_disclaimer(report: str) -> str:
    """Guarantee the disclaimer footer is present regardless of LLM output."""
    if "This is not financial advice" not in report:
        return report + _DISCLAIMER
    return report


def _check_quality(report: str) -> list[str]:
    """Return list of section names that fall below minimum word-count thresholds.

    Splits the report at heading boundaries and counts words in each section.
    Logs a warning for each under-length section but does NOT fail the report.
    """
    under_threshold: list[str] = []

    # Build (heading_text, start_index) pairs from all headings in the report
    matches = list(_SECTION_HEADING_RE.finditer(report))
    if not matches:
        return under_threshold

    for i, match in enumerate(matches):
        heading_text = match.group(1).strip()
        section_start = match.end()
        section_end = matches[i + 1].start() if i + 1 < len(matches) else len(report)
        section_body = report[section_start:section_end]
        word_count = len(section_body.split())

        # Find the canonical section name this heading maps to (partial match)
        for canonical, min_words in _MIN_SECTION_WORDS.items():
            if canonical.lower() in heading_text.lower():
                if word_count < min_words:
                    logger.warning(
                        "Quality check: '%s' has %d words (minimum %d)",
                        canonical, word_count, min_words,
                    )
                    under_threshold.append(canonical)
                break

    return under_threshold
